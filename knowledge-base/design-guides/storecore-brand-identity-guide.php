    <h2 id="brand-colours">Brand colours</h2>
    <p>All colours for the StoreCore brand identity and StoreCore user
      interfaces are based on the original colour palettes from 2014 for <a
      href="https://material.io/" rel="noreferrer noopener"
      title="Material Design">Material Design</a> by Google.</p>

    <amp-youtube data-videoid="xYkz0Ueg0L4" layout="responsive" width="480" height="270"></amp-youtube>

    <p>In Material Design a <dfn>primary colour</dfn> is the colour displayed
      most frequently across an app’s screens and components.  The primary
      colour for StoreCore is <strong>Light Green 500</strong> or
      hexadecimal <code>#8bc34a</code> in CSS.</p>

    <amp-img alt="Light Green 500" height="204" src="Light-Green-500.png" width="204"></amp-img>

    <p>A <dfn>secondary colour</dfn> provides additional ways to accent a product.
      Although having a secondary colour is optional in Material Design, it may be
      applied to accent select parts of a user interface, like action buttons,
      selection controls, links and headlines.  The secondary colour for StoreCore
      is <strong>Light Blue 500</strong> or hexadecimal <code>#03a9f4</code>.</p>

    <div>
      <amp-img alt="Light Green 500" height="204" src="Light-Green-500.png" width="204"></amp-img>
      <amp-img alt="Light Blue 500" height="204" src="Light-Blue-500.png" width="204"></amp-img>
    </div>

    <p>For subtle accents and additional colour depth, the two 500 base colours
      are accompanied by their darker variants <strong>Light Green 700</strong>
      or hexadecimal <code>#689f38</code> and <strong>Light Blue 700</strong> or
      <code>#0288d1</code>.</p>

    <div>
      <amp-img alt="Light Green 500" height="204" src="Light-Green-500.png" width="204"></amp-img>
      <amp-img alt="Light Green 700" height="204" src="Light-Green-700.png" width="204"></amp-img>
    </div>
    <div>
      <amp-img alt="Light Blue 500" height="204" src="Light-Blue-500.png" width="204"></amp-img>
      <amp-img alt="Light Blue 700" height="204" src="Light-Blue-700.png" width="204"></amp-img>
    </div>

    <p>Finally, these subtle, natural colours are offset by an
      <dfn>error colour</dfn> for a state in which a system is off it’s feet.
      In the StoreCore palette this is <strong>Red 700</strong> or hexadecimal
      <code>#d32f2f</code>, a counterpart of the Material colours Light Green
      700 and Light Blue 700.  This basic design system allows for functional
      and meaningful <abbr title="red, green, and blue">RGB</abbr> colours in
      user interfaces: green for good, red for bad, and blue for neutral
      information.</p>
    <div>
      <amp-img alt="Red 700" height="204" src="Red-700.png" width="204"></amp-img>
      <amp-img alt="Light Green 700" height="204" src="Light-Green-700.png" width="204"></amp-img>
