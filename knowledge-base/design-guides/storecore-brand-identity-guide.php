    <h2 id="brand-logotype">Brand logotype</h2>
    <p>The StoreCore logotype consists of the word <em>Store</em> in Roboto
      Black with <code>font-weight: 900</code> and the word <em>Core</em> in
      Roboto Light with <code>font-weight: 300</code>.  <em>StoreCore</em> is
      always written as one word with a capital S and C, without a space or
      hyphen.</p>

    <div class="amd-theme-logotype"><strong>Store</strong>Core</div>

    <p>The basic <abbr title="Cascading Style Sheets">CSS</abbr> code for the
      StoreCore logotype is:</p>

<pre><code>div {
  color: #000;
  font-family: Roboto, sans-serif;
  font-size: 60px;
  font-style: normal;
  font-weight: 300;
  letter-spacing: -0.02em;
}

strong {
  font-style: normal;
  font-weight: 900;
}</code></pre>

    <p>The following colour combinations are allowed for the logotype:</p>
    <ul>
      <li>black text on a white background</li>
      <li>white text on a black background</li>
      <li>white text on a Material Light Green 500 background</li>
      <li>Material Light Green 500 text on a white background</li>
    </ul>
    <p>Instead of true black <code>#000000</code> or <code>#000</code> in
      <abbr title="Cascading Style Sheets">CSS</abbr>, a very dark grey like
      <code>#222</code> or <code>rgba(0, 0, 0, 0.87)</code> from the
      Material Design specification may be used.</p>

